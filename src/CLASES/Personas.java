package CLASES;

public class Personas {

	String name, direction;
	int ID;
	
	public Personas(String name, String direction, int iD) {
		this.name = name;
		this.direction = direction;
		ID = iD;
	
	}

	public String getName() {
		return name;
	}

	public String getDirection() {
		return direction;
	}

	public int getID() {
		return ID;
	}


	
	
}
